"""Export GLM-OCR (zai-org/GLM-OCR) to a 4-part ONNX model suite.

## Architecture

GLM-OCR is a vision-language model (VLM) built on Qwen2-VL's architecture.
The HuggingFace model (`AutoModelForImageTextToText`) contains:

    ┌─ model.visual          — ViT vision encoder with M-RoPE (Multi-Resolution
    │                          Rotary Position Embeddings)
    ├─ model.language_model  — Qwen2-based decoder-only LLM (16 layers, GQA)
    │   └─ embed_tokens      — token embedding table (59392 tokens × 1536 dim)
    └─ lm_head               — linear projection → vocab logits

We export this as 4 separate ONNX files:

1. **vision_encoder_mha.onnx** — Exported from native BF16 weights, MHA-optimized.
   MultiHeadAttention fusion for FlashAttention on CUDA.
   Input: pixel_values, pre-computed M-RoPE position IDs, max_grid_size.
   Output: vision hidden states [num_merged_patches, 1536].

2. **embedding.onnx** — Token embedding lookup, split out so the Rust
   runtime can embed tokens without loading the full LLM.
   Input: input_ids [batch, seq_len].
   Output: inputs_embeds [batch, seq_len, 1536].

3. **llm.onnx** — The LLM with dynamic KV cache. Used for the prefill pass
   (single call with full prompt + vision embeddings).
   Input: inputs_embeds, attention_mask, 3D position_ids, past KV cache.
   Output: logits [batch, seq_len, vocab], updated KV cache.

4. **llm_decoder_gqa.onnx** — CUDA-graph-friendly decode-step LLM with
   GroupQueryAttention fusion for FlashAttention V2. Fixed-size KV cache,
   all tensor shapes static for CUDA graph capture/replay.
   Input: input_ids [1,1], step counter, prefill_len, fixed KV cache.
   Output: next_token [1] (ArgMax built in), updated KV cache.

## Inference flow (Rust runtime)

    Image → HF Processor → pixel_values, input_ids
    pixel_values → vision_encoder.onnx → vision_embeds
    input_ids → embedding.onnx → token_embeds
    Merge vision_embeds into token_embeds at <image> positions
    merged_embeds → llm.onnx (prefill, 1 call) → first logits + KV cache
    token → llm_decoder.onnx (decode loop, CUDA graph) → next tokens

## Vision encoder export strategy

The vision encoder's rot_pos_emb() uses Python loops with data-dependent
shapes (for t, h, w in grid_thw) that torch.onnx.export can't trace. We
work around this by pre-computing position IDs outside the ONNX model:
the wrapper takes pos_ids [N, 2] and max_grid_size as inputs (computed in
the Rust runtime). Attention is also inlined as standard matmul attention
to avoid the untraceable cu_seqlens splitting path.

## Precision modes

- **BF16** (default): Native model precision. All models exported directly
  from BF16 weights. Vision encoder Conv/LayerNorm/etc. ops wrapped with FP32
  Cast nodes (ONNX schema rejects BF16 for these). Requires CUDA SM80+.

- **FP16**: Wider ORT compatibility. Lossy conversion from native BF16.

- **FP32**: Debug/validation only. Larger files, slower inference.

## Processor and config files

`export.py` also saves the HuggingFace processor and config to the model
directory via `save_pretrained()`. This produces:

    tokenizer.json, tokenizer_config.json   — BPE tokenizer (59392 vocab)
    config.json                              — model architecture config
    processor_config.json                    — image preprocessor config
    chat_template.jinja                      — chat prompt template

These are needed by both the Python inference scripts (which use
`AutoProcessor.from_pretrained(model_dir)`) and the Rust runtime (which
reads `tokenizer.json` directly via the `tokenizers` crate). They are
auto-generated by this script and gitignored.

Usage:
  python export.py --bf16                # BF16 export (recommended, CUDA SM80+)
  python export.py                       # FP16 export (wider compatibility)
  python export.py --fp32                # FP32 export (debug)
  python export.py --skip-vision         # skip vision encoder
  python export.py --skip-decoder        # skip decoder + GQA optimization
  python export.py --max-seq 256         # decoder max sequence length
"""

