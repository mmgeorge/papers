class VisionEncoderWrapper(nn.Module):
    """Wraps the vision encoder for ONNX export with pre-computed position IDs.

    The vision encoder's rot_pos_emb() uses Python loops with data-dependent
    shapes (for t, h, w in grid_thw) that torch.onnx.export can't trace.
    This wrapper takes pre-computed pos_ids as input instead, avoiding all
    data-dependent control flow.

    For single-image inference, attention is full (no cu_seqlens chunking).
    The eager attention path in GlmOcrVisionAttention splits by cu_seqlens
    using torch.split(lengths.tolist()), which is also untraceable. We bypass
    this by re-implementing attention inline with standard matmul attention.

    Inputs:
        pixel_values:  [num_patches, 1176] bf16 — flattened image patches
        pos_ids:       [num_patches, 2] int64 — pre-computed (h, w) position pairs
        max_grid_size: [] int64 scalar — max(h, w) for RoPE frequency table

    Output:
        hidden_states: [num_merged_patches, 1536] bf16 — vision embeddings
    """

    def __init__(self, visual_model):
        super().__init__()
        self.patch_embed = visual_model.patch_embed
        self.rotary_pos_emb = visual_model.rotary_pos_emb
        self.blocks = visual_model.blocks
        self.post_layernorm = visual_model.post_layernorm
        self.downsample = visual_model.downsample
        self.merger = visual_model.merger

        # Copy attention config from first block for inline attention
        attn = visual_model.blocks[0].attn
        self._num_heads = attn.num_heads
        self._head_dim = attn.head_dim
        self._scaling = attn.scaling

    def _attention_forward(self, attn_module, hidden_states, position_embeddings):
        """Inline attention: QKV projection → RMSNorm → RoPE → matmul attention.

        Replaces GlmOcrVisionAttention.forward() to avoid the untraceable
        cu_seqlens splitting path. Uses standard scaled dot-product attention
        over the full sequence (single-image, no chunking needed).
        """
        seq_len = hidden_states.shape[0]

        # QKV projection + reshape to [Q, K, V] each [seq, heads, head_dim]
        qkv = attn_module.qkv(hidden_states)
        q, k, v = qkv.reshape(seq_len, 3, self._num_heads, self._head_dim).permute(1, 0, 2, 3).unbind(0)

        # Per-head RMSNorm (applied before RoPE, unusual for this architecture)
        q = attn_module.q_norm(q)
        k = attn_module.k_norm(k)

        # 2D RoPE
        from transformers.models.glm_ocr.modeling_glm_ocr import apply_rotary_pos_emb_vision
        cos, sin = position_embeddings
        q, k = apply_rotary_pos_emb_vision(q, k, cos, sin)

        # Reshape for batched matmul: [seq, heads, dim] → [1, heads, seq, dim]
        q = q.transpose(0, 1).unsqueeze(0)
        k = k.transpose(0, 1).unsqueeze(0)
        v = v.transpose(0, 1).unsqueeze(0)

        # Standard scaled dot-product attention (no mask — bidirectional)
        attn_weights = torch.matmul(q, k.transpose(2, 3)) * self._scaling
        attn_weights = torch.nn.functional.softmax(attn_weights, dim=-1, dtype=torch.float32).to(q.dtype)
        attn_output = torch.matmul(attn_weights, v)

        # Reshape back: [1, heads, seq, dim] → [seq, hidden]
        attn_output = attn_output.squeeze(0).transpose(0, 1).reshape(seq_len, -1).contiguous()
        return attn_module.proj(attn_output)

    def forward(self, pixel_values, pos_ids, max_grid_size):
        hidden_states = self.patch_embed(pixel_values)

        # Compute RoPE from pre-computed pos_ids (no data-dependent shapes)
        freqs = self.rotary_pos_emb(max_grid_size)  # [max_grid, head_dim//2]
        rope = freqs[pos_ids].flatten(1)             # [N, head_dim]
        emb = torch.cat((rope, rope), dim=-1)        # [N, head_dim*2]
        position_embeddings = (emb.cos(), emb.sin())

        # Run 24 blocks with inline attention (bypasses cu_seqlens splitting)
        for blk in self.blocks:
            hidden_states = hidden_states + self._attention_forward(
                blk.attn, blk.norm1(hidden_states), position_embeddings,
            )
            hidden_states = hidden_states + blk.mlp(blk.norm2(hidden_states))

        # Post-layernorm + spatial downsample (2×2 merge via Conv2d)
        hidden_states = self.post_layernorm(hidden_states)
        hidden_states = hidden_states.view(-1, 2, 2, hidden_states.shape[-1])
        hidden_states = hidden_states.permute(0, 3, 1, 2)
        hidden_states = self.downsample(hidden_states).view(-1, self.downsample.out_channels)

        # Patch merger (gated MLP)
        return self.merger(hidden_states)


def export_vision_encoder(model, output_dir: Path, dtype):
    """Export the vision encoder directly from the model's native weights.

    Pipeline: wrap visual model → trace in FP32 → MHA optimization →
    convert FP32→BF16 (direct, no FP16 intermediate).

    Tracing in FP32 produces a clean graph that MHA optimization can
    pattern-match on. The FP32→BF16 conversion is lossless for native BF16
    weights (BF16→FP32 upcast for tracing is exact, FP32→BF16 rounds back
    to the same values). Ops without BF16 support in the ONNX schema (Conv,
    LayerNorm, etc.) stay in FP32 with Cast wrappers.

    The wrapper moves M-RoPE position computation outside the ONNX model
    (pos_ids and max_grid_size become inputs), avoiding data-dependent shapes
    that torch.onnx.export can't trace. Attention is inlined as standard
    matmul attention (no cu_seqlens chunking).

    MHA optimization runs on the FP32 graph to fuse attention ops into
    com.microsoft.MultiHeadAttention for FlashAttention on CUDA.
    """
    from optimize_mha_vision import apply_mha_surgery

    print("Exporting vision_encoder_mha.onnx ...")
    # Trace in FP32 for clean graph structure (MHA surgery needs FP32 patterns)
    wrapper = VisionEncoderWrapper(model.model.visual).eval().to(torch.float32)

    # Example inputs: 24×24 grid = 576 patches (standard 336×336 image)
    num_patches = 576
    pixel_values = torch.randn(num_patches, 1176, dtype=torch.float32)
    pos_ids = torch.zeros(num_patches, 2, dtype=torch.long)
    max_grid_size = torch.tensor(24, dtype=torch.long)

    # Fill pos_ids with realistic values for tracing
    idx = 0
    for bh in range(12):
        for bw in range(12):
            for mh in range(2):
                for mw in range(2):
                    pos_ids[idx, 0] = bh * 2 + mh
                    pos_ids[idx, 1] = bw * 2 + mw
                    idx += 1

    raw_path = output_dir / "vision_encoder_raw.onnx"
    mha_path = output_dir / "vision_encoder_mha.onnx"

    torch.onnx.export(
        wrapper,
        (pixel_values, pos_ids, max_grid_size),
        str(raw_path),
        opset_version=17,
        input_names=["pixel_values", "pos_ids", "max_grid_size"],
        output_names=["hidden_states"],
        dynamic_axes={
            "pixel_values": {0: "num_patches"},
            "pos_ids": {0: "num_patches"},
            "hidden_states": {0: "num_merged_patches"},
        },
    )
    _print_size(raw_path)

    # MHA optimization: fuse attention into com.microsoft.MultiHeadAttention
    print("  Applying MHA surgery ...")
    apply_mha_surgery(raw_path, mha_path)
    raw_path.unlink()
    raw_data = raw_path.with_suffix(".onnx.data")
    if raw_data.exists():
        raw_data.unlink()

    # Convert FP32→BF16 (direct, no FP16 intermediate)
    if dtype == torch.bfloat16:
        print("  Converting FP32 → BF16 ...")
        _convert_fp32_to_bf16(mha_path)

    _print_size(mha_path)
    return mha_path


def _convert_fp32_to_bf16(onnx_path: Path):
    """Convert an FP32 ONNX graph to BF16 in-place, with FP32 islands.

    Direct FP32→BF16 conversion (no FP16 intermediate). For ops that lack
    BF16 CUDA kernels (Conv, LayerNorm, Range, etc.), weights stay FP32 and
    Cast nodes wrap their inputs/outputs. FP32 preserves full BF16 precision
    (unlike FP16 which has a narrower exponent range).

    This is lossless for native BF16 weights: the FP32 graph was traced
    from BF16 weights (BF16→FP32 upcast is exact), so FP32→BF16 rounds
    back to the original values.
    """
    from ml_dtypes import bfloat16 as ml_bf16

    FLOAT = onnx.TensorProto.FLOAT        # 1
    BFLOAT16 = onnx.TensorProto.BFLOAT16  # 16

    # Ops without BF16 support in ORT 1.24 CUDA EP (ONNX type constraints reject
    # bfloat16). Fall back to FP32 (not FP16) to preserve native BF16 precision.
    # Tested empirically; see PORTING.md for the full compatibility table.
    FP32_OPS = {
        "Conv", "Einsum", "LayerNormalization", "Range",
        "Sin", "Cos", "Reciprocal", "Squeeze", "ReduceMean",
    }

    model = onnx.load(str(onnx_path))

    # 1. Catalog non-BF16 nodes and their input tensor names
    fp32_input_names = set()   # inputs to FP32 ops (initializers stay FP32)
    non_bf16_count = 0
    for node in model.graph.node:
        if node.op_type in FP32_OPS:
            non_bf16_count += 1
            for name in node.input:
                if name:
                    fp32_input_names.add(name)
    print(f"    {non_bf16_count} non-BF16 node(s)")

    # Build maps
    init_map = {init.name: init for init in model.graph.initializer}
    vi_map = {}
    for vi in list(model.graph.value_info) + list(model.graph.input) + list(model.graph.output):
        vi_map[vi.name] = vi

    # 2. Identify initializers consumed ONLY by FP32 ops (not shared with BF16 ops).
    # These stay FP32; shared initializers become BF16 (Cast nodes handle FP32 ops).
    all_input_names = set()
    for node in model.graph.node:
        for name in node.input:
            if name:
                all_input_names.add(name)

    fp32_only_inits = set()
    for name in fp32_input_names:
        if name in init_map:
            # Check if this init is consumed by ANY non-FP32 op
            shared_with_bf16 = False
            for node in model.graph.node:
                if node.op_type not in FP32_OPS and name in list(node.input):
                    shared_with_bf16 = True
                    break
            if not shared_with_bf16:
                fp32_only_inits.add(name)

    n_bf16 = 0
    n_fp32_kept = 0
    for init in model.graph.initializer:
        if init.data_type != FLOAT:
            continue
        if init.name in fp32_only_inits:
            # Exclusively consumed by FP32 ops → keep FP32
            n_fp32_kept += 1
        else:
            # Everything else (including shared) → BF16
            raw = np.frombuffer(init.raw_data, dtype=np.float32)
            init.raw_data = raw.astype(ml_bf16).tobytes()
            init.data_type = BFLOAT16
            n_bf16 += 1

    # 3. Update all FP32 type annotations to BF16
    # (FP32 op outputs get BF16 too — Cast nodes in step 5 bridge the gap)
    n_vi = 0
    for vi in list(model.graph.value_info) + list(model.graph.input) + list(model.graph.output):
        if vi.type.tensor_type.elem_type == FLOAT:
            vi.type.tensor_type.elem_type = BFLOAT16
            n_vi += 1

    # 4. Update Cast nodes: Cast(to=FLOAT) → Cast(to=BF16)
    n_cast = 0
    for node in model.graph.node:
        if node.op_type == "Cast":
            for attr in node.attribute:
                if attr.name == "to" and attr.i == FLOAT:
                    attr.i = BFLOAT16
                    n_cast += 1

    print(f"    {n_bf16} init→BF16, {n_fp32_kept} init kept FP32, {n_vi} annotations, {n_cast} Casts")

    # 5. Insert Cast nodes around non-BF16 ops (BF16↔FP32)
    original_nodes = list(model.graph.node)
    new_nodes = []
    # Track already-inserted BF16→FP32 casts to avoid duplicates when
    # multiple FP32 ops share the same input tensor.
    bf16_to_fp32_casts = {}  # inp_name → cast_out_name

    for node in original_nodes:
        if node.op_type not in FP32_OPS:
            new_nodes.append(node)
            continue

        # Cast(BF16→FP32) for each BF16 input (including shared BF16 initializers)
        for i, inp_name in enumerate(node.input):
            if not inp_name:
                continue
            # Skip FP32-only initializers (already FP32, no cast needed)
            if inp_name in fp32_only_inits:
                continue
            # Skip non-BF16 initializers
            if inp_name in init_map and init_map[inp_name].data_type != BFLOAT16:
                continue
            # Check if it's a BF16 tensor (value_info or initializer)
            is_bf16 = False
            if inp_name in vi_map and vi_map[inp_name].type.tensor_type.elem_type == BFLOAT16:
                is_bf16 = True
            elif inp_name in init_map and init_map[inp_name].data_type == BFLOAT16:
                is_bf16 = True
            if not is_bf16:
                continue
            if inp_name in bf16_to_fp32_casts:
                # Reuse existing Cast node
                node.input[i] = bf16_to_fp32_casts[inp_name]
            else:
                cast_out = f"{inp_name}__to_fp32"
                cast_node = onnx.helper.make_node(
                    "Cast", inputs=[inp_name], outputs=[cast_out],
                    to=FLOAT, name=f"Cast_bf16_to_fp32_{inp_name}",
                )
                new_vi = model.graph.value_info.add()
                new_vi.name = cast_out
                new_vi.type.tensor_type.elem_type = FLOAT
                if inp_name in vi_map and vi_map[inp_name].type.tensor_type.HasField('shape'):
                    new_vi.type.tensor_type.shape.CopyFrom(
                        vi_map[inp_name].type.tensor_type.shape)
                node.input[i] = cast_out
                new_nodes.append(cast_node)
                bf16_to_fp32_casts[inp_name] = cast_out

        new_nodes.append(node)

        # Cast(FP32→BF16) for each output so downstream BF16 nodes get BF16 data
        for i, out_name in enumerate(node.output):
            if not out_name:
                continue
            tmp_name = f"{out_name}__fp32_out"
            # Create FP32 value_info for the op's actual output
            new_vi = model.graph.value_info.add()
            new_vi.name = tmp_name
            new_vi.type.tensor_type.elem_type = FLOAT
            if out_name in vi_map and vi_map[out_name].type.tensor_type.HasField('shape'):
                new_vi.type.tensor_type.shape.CopyFrom(
                    vi_map[out_name].type.tensor_type.shape)
            cast_node = onnx.helper.make_node(
                "Cast", inputs=[tmp_name], outputs=[out_name],
                to=BFLOAT16, name=f"Cast_fp32_to_bf16_{out_name}",
            )
            node.output[i] = tmp_name
            new_nodes.append(cast_node)

    del model.graph.node[:]
    model.graph.node.extend(new_nodes)

    # Delete old external data file before saving to avoid stale/appended data.
    data_path = Path(str(onnx_path) + ".data")
    if data_path.exists():
        data_path.unlink()

    # Externalize tensors >= 1 KB to avoid padding overhead on tiny tensors.
    # convert_attribute=True ensures Constant node data is also saved externally.
    onnx.save(
        model, str(onnx_path),
        save_as_external_data=True,
        all_tensors_to_one_file=True,
        location=onnx_path.name + ".data",
        size_threshold=1024,
        convert_attribute=True,
    )


