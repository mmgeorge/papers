# ── Wrapper modules ──────────────────────────────────────────────────────
#
# torch.onnx.export works by TRACING: it runs a PyTorch nn.Module's forward()
# with example inputs, records every tensor operation, and writes those ops
# as ONNX nodes. The resulting ONNX graph is a flattened fusion of ALL code
# that executes during the trace — including HuggingFace library internals,
# our wrapper logic, and the model weights.
#
# The problem: HuggingFace's API uses Python objects (DynamicCache, StaticCache),
# keyword arguments, and optional returns. ONNX only supports flat tensor
# inputs and outputs. These wrappers bridge that gap:
#
#   ONNX flat tensors → wrapper.forward() → HuggingFace Python API → wrapper → ONNX flat tensors
#
# Each wrapper reconstructs the Python objects HuggingFace expects, calls the
# model, then flattens the results back to tensors. The trace captures
# everything that happens inside forward(), including the HuggingFace code.


class EmbedTokensWrapper(nn.Module):
    """Wraps the text embedding layer for standalone ONNX export.

    Extracting embedding as a separate ONNX model lets the Rust runtime
    do token→embedding lookup independently from the LLM forward pass.
    This is used during prefill (embed all prompt tokens at once) and
    also by the non-CUDA-graph decode path (embed one token per step).

    The CUDA-graph decoder (llm_decoder.onnx, see DecoderStepWrapper)
    has the embedding table baked in, so it doesn't need this.
    """

    def __init__(self, model):
        super().__init__()
        self.embed_tokens = model.model.language_model.embed_tokens

    def forward(self, input_ids):
        return self.embed_tokens(input_ids)


class LLMWrapper(nn.Module):
    """Wraps the LLM decoder with explicit flattened KV cache I/O.

    HuggingFace's LLM expects a `DynamicCache` object for KV cache, but
    ONNX only supports flat tensor inputs/outputs. This wrapper:

    1. Takes 32 individual past_key/past_value tensors (16 layers × 2)
       as explicit named parameters
    2. Reconstructs a DynamicCache from them
    3. Calls the HuggingFace LLM
    4. Returns logits + 32 updated KV tensors as a flat tuple

    Why explicit named parameters instead of *args:
    torch.onnx.export in torch 2.10+ has issues mapping dynamic_axes to
    positional *args parameters. Using explicit named parameters
    (past_key_0, past_value_0, ...) ensures dynamic_axes dict keys
    match the actual parameter names reliably.

    The dynamic KV cache means past_seq_len grows by 1 each decode step.
    This is fine for the prefill pass (run once) but incompatible with
    CUDA graphs (which need fixed shapes). See DecoderStepWrapper for the
    fixed-shape alternative used in the decode loop.
    """

    def __init__(self, model):
        super().__init__()
        self.language_model = model.model.language_model
        self.lm_head = model.lm_head

    def forward(self, inputs_embeds, attention_mask, position_ids,
                past_key_0, past_value_0, past_key_1, past_value_1,
                past_key_2, past_value_2, past_key_3, past_value_3,
                past_key_4, past_value_4, past_key_5, past_value_5,
                past_key_6, past_value_6, past_key_7, past_value_7,
                past_key_8, past_value_8, past_key_9, past_value_9,
                past_key_10, past_value_10, past_key_11, past_value_11,
                past_key_12, past_value_12, past_key_13, past_value_13,
                past_key_14, past_value_14, past_key_15, past_value_15):
        past_keys = [past_key_0, past_key_1, past_key_2, past_key_3,
                     past_key_4, past_key_5, past_key_6, past_key_7,
                     past_key_8, past_key_9, past_key_10, past_key_11,
                     past_key_12, past_key_13, past_key_14, past_key_15]
        past_values = [past_value_0, past_value_1, past_value_2, past_value_3,
                       past_value_4, past_value_5, past_value_6, past_value_7,
                       past_value_8, past_value_9, past_value_10, past_value_11,
                       past_value_12, past_value_13, past_value_14, past_value_15]

        # Reconstruct HuggingFace's DynamicCache from flat tensors.
        # DynamicCache stores a list of (key, value) per layer and handles
        # concatenation of new KV pairs during generation.
        past_key_values = DynamicCache()
        for layer in range(NUM_LAYERS):
            past_key_values.update(past_keys[layer], past_values[layer], layer)

        outputs = self.language_model(
            inputs_embeds=inputs_embeds,
            attention_mask=attention_mask,
            position_ids=position_ids,
            past_key_values=past_key_values,
            use_cache=True,
        )

        logits = self.lm_head(outputs.last_hidden_state)
        new_past = outputs.past_key_values

        # Flatten updated KV cache back to individual tensors for ONNX output.
        # Order: logits, key_0, value_0, key_1, value_1, ...
        result = [logits]
        for layer in range(NUM_LAYERS):
            result.append(new_past.layers[layer].keys)
            result.append(new_past.layers[layer].values)
        return tuple(result)


class WhereStaticLayer(StaticLayer):
    """StaticLayer that uses torch.where instead of index_copy_ for KV updates.

    HuggingFace's default StaticLayer uses `index_copy_()` to write new KV
    entries into the cache buffer at a given position. `index_copy_` exports
    to ONNX as ScatterND, which does indirect addressing (index tensor
    determines write location). Indirect addressing is incompatible with
    CUDA graphs because the index values are read from GPU memory — if the
    buffer address changes between capture and replay, ORT reads garbage
    indices and crashes with ILLEGAL_ADDRESS.

    torch.where exports to ONNX as the Where op, which uses a boolean mask
    computed from a simple comparison (position == cache_position). This is
    fully deterministic and CUDA-graph compatible.
    """

    def update(self, key_states, value_states, cache_kwargs=None):
        if not self.is_initialized:
            self.lazy_initialization(key_states, value_states)

        cache_position = cache_kwargs.get("cache_position") if cache_kwargs is not None else None
        cache_position = (
            cache_position if cache_position is not None else torch.arange(key_states.shape[-2], device=self.device)
        )
        # Build a boolean mask: [1, 1, MAX_SEQ, 1] — True only at cache_position
        positions = torch.arange(self.keys.shape[2], device=self.keys.device)
        mask = (positions == cache_position.squeeze()).view(1, 1, -1, 1)
        # Use Where to do the update: replace at cache_position, keep rest
        self.keys = torch.where(mask, key_states.expand_as(self.keys), self.keys)
        self.values = torch.where(mask, value_states.expand_as(self.values), self.values)
        return self.keys, self.values


class WhereStaticCache(StaticCache):
    """StaticCache using WhereStaticLayer for CUDA-graph-compatible updates."""

    def __init__(self, config, max_cache_len):
        super().__init__(config, max_cache_len=max_cache_len)
        for i, layer in enumerate(self.layers):
            new_layer = WhereStaticLayer(max_cache_len=max_cache_len)
            if layer.is_initialized:
                new_layer.keys = layer.keys
                new_layer.values = layer.values
                new_layer.is_initialized = True
                new_layer.dtype = layer.dtype
                new_layer.device = layer.device
                new_layer.max_batch_size = layer.max_batch_size
                new_layer.num_heads = layer.num_heads
                new_layer.k_head_dim = layer.k_head_dim
                new_layer.v_head_dim = layer.v_head_dim
            self.layers[i] = new_layer


class DecoderStepWrapper(nn.Module):
    """Wraps the GLM-OCR LLM for a single decode step with fixed-size KV cache.

    This is the CUDA-graph-compatible counterpart to LLMWrapper. Key differences:

    | Aspect         | LLMWrapper (prefill)           | DecoderStepWrapper (decode)     |
    |----------------|--------------------------------|---------------------------------|
    | KV cache       | Dynamic (grows each step)      | Fixed MAX_SEQ (write by index)  |
    | Cache type     | DynamicCache (append)          | WhereStaticCache (overwrite)    |
    | Input          | inputs_embeds (pre-embedded)   | input_ids (embeds internally)   |
    | Output         | Full logits [1, seq, 59392]    | next_token [1] (ArgMax built in)|
    | Position IDs   | External (caller computes)     | Internal (derives from step)    |
    | Attention mask | External 2D (caller passes)    | Internal 4D causal mask         |
    | CUDA graph     | Incompatible (dynamic shapes)  | Compatible (all shapes fixed)   |

    The 4D attention mask is computed internally to avoid GatherND. HuggingFace's
    `create_causal_mask()` generates GatherND when building a 4D mask from a 2D
    mask during ONNX tracing. GatherND does indirect addressing which breaks
    CUDA graphs (same reason as ScatterND). By passing a pre-computed 4D mask,
    we bypass create_causal_mask() entirely (it has an early-exit for 4D input
    at masking_utils.py:788).

    Inputs:
        input_ids:      [1, 1] int64 — single token to decode
        step:           [1] int64 — decode step (0 = first generated token)
        prefill_len:    [1] int64 — length of the prefill KV cache
        past_key_{i}:   [1, 8, MAX_SEQ, 128] × 16 layers
        past_value_{i}: [1, 8, MAX_SEQ, 128] × 16 layers

    Outputs:
        next_token:       [1] int64 — argmax of logits (stays on GPU)
        present_key_{i}:  [1, 8, MAX_SEQ, 128] × 16 layers
        present_value_{i}:[1, 8, MAX_SEQ, 128] × 16 layers
    """

    def __init__(self, model, max_seq: int):
        super().__init__()
        self.embed_tokens = model.model.language_model.embed_tokens
        self.language_model = model.model.language_model
        self.lm_head = model.lm_head
        self.max_seq = max_seq
        self.config = model.config.text_config

    def forward(self, input_ids, step, prefill_len,
                past_key_0, past_value_0, past_key_1, past_value_1,
                past_key_2, past_value_2, past_key_3, past_value_3,
                past_key_4, past_value_4, past_key_5, past_value_5,
                past_key_6, past_value_6, past_key_7, past_value_7,
                past_key_8, past_value_8, past_key_9, past_value_9,
                past_key_10, past_value_10, past_key_11, past_value_11,
                past_key_12, past_value_12, past_key_13, past_value_13,
                past_key_14, past_value_14, past_key_15, past_value_15):

        past_keys = [past_key_0, past_key_1, past_key_2, past_key_3,
                     past_key_4, past_key_5, past_key_6, past_key_7,
                     past_key_8, past_key_9, past_key_10, past_key_11,
                     past_key_12, past_key_13, past_key_14, past_key_15]
        past_values = [past_value_0, past_value_1, past_value_2, past_value_3,
                       past_value_4, past_value_5, past_value_6, past_value_7,
                       past_value_8, past_value_9, past_value_10, past_value_11,
                       past_value_12, past_value_13, past_value_14, past_value_15]

        # Embed the single input token (baked into graph, no separate session call)
        inputs_embeds = self.embed_tokens(input_ids)  # [1, 1, HIDDEN_SIZE]

        # Compute write position in the KV cache
        cache_pos = prefill_len + step  # [1] — where to write new KV entry

        # Build 4D causal attention mask directly, bypassing create_causal_mask()
        # which generates a GatherND op that breaks CUDA graph capture.
        # Shape: [1, 1, 1, MAX_SEQ] — 0.0 for attended, min_dtype for masked
        min_dtype = torch.finfo(inputs_embeds.dtype).min
        positions = torch.arange(self.max_seq, device=input_ids.device)
        attend = positions < (cache_pos + 1)  # [MAX_SEQ] bool
        attention_mask = torch.where(
            attend.view(1, 1, 1, -1),
            torch.tensor(0.0, dtype=inputs_embeds.dtype, device=input_ids.device),
            torch.tensor(min_dtype, dtype=inputs_embeds.dtype, device=input_ids.device),
        )

        # Build 3D M-RoPE position IDs for decode step.
        # During decode, all 3 dims (temporal, height, width) get the same value
        # because we're generating text tokens (not vision tokens).
        position_ids = cache_pos.view(1, 1, 1).expand(3, 1, 1).contiguous()

        # Build WhereStaticCache (uses Where op instead of ScatterND)
        past_key_values = WhereStaticCache(
            config=self.config,
            max_cache_len=self.max_seq,
        )

        # Inject the input KV buffers into the cache layers
        for layer_idx in range(NUM_LAYERS):
            layer = past_key_values.layers[layer_idx]
            layer.keys = past_keys[layer_idx]
            layer.values = past_values[layer_idx]
            layer.is_initialized = True
            layer.dtype = past_keys[layer_idx].dtype
            layer.device = past_keys[layer_idx].device
            layer.max_batch_size = 1
            layer.num_heads = NUM_KV_HEADS
            layer.k_head_dim = HEAD_DIM
            layer.v_head_dim = HEAD_DIM

        # Run the language model with WhereStaticCache + cache_position
        outputs = self.language_model(
            inputs_embeds=inputs_embeds,
            attention_mask=attention_mask,
            position_ids=position_ids,
            past_key_values=past_key_values,
            use_cache=True,
            cache_position=cache_pos,
        )

        logits = self.lm_head(outputs.last_hidden_state)  # [1, 1, VOCAB_SIZE]

        # ArgMax for next token — stays on GPU, only 8 bytes to copy per step
        next_token = logits[:, -1, :].argmax(dim=-1)  # [1]

        # Extract updated KV cache
        updated_cache = outputs.past_key_values
        result = [next_token]
        for layer_idx in range(NUM_LAYERS):
            result.append(updated_cache.layers[layer_idx].keys)
            result.append(updated_cache.layers[layer_idx].values)

        return tuple(result)


