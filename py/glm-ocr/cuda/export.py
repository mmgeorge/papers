def export_vision_encoder(output_dir: Path, precision: str = "fp16"):
    """Download vision encoder from ningpp/GLM-OCR and convert to target precision.

    The vision encoder can't be re-exported from PyTorch because M-RoPE uses
    data-dependent shapes (torch.split with dynamic lengths from image_grid_thw)
    that torch.onnx.export can't trace into a single static graph. We use the
    community FP32 export from HuggingFace and convert it.

    FP16 conversion details:
      The FP32 model contains Cast-to-FLOAT nodes that upcast activations for
      ops requiring FP32 precision (Conv, Range, LayerNorm). When converting
      to FP16, onnxconverter_common would naively convert these Cast targets
      too, breaking the type chain (a Cast(to=FP16) feeding into a node that
      expects FP32 input). We block all Cast-to-FLOAT nodes to preserve them.

      Side effect: ORT's SimplifiedLayerNormFusion pattern-matching breaks on
      the resulting mixed-type graph. Use ORT_ENABLE_EXTENDED (not ENABLE_ALL)
      at runtime for this model.

    BF16 conversion details:
      First converts FP32→FP16, then FP16→BF16 with special handling for Conv
      nodes. ORT has no BF16 Conv kernel on any execution provider, so Conv
      ops must stay in FP16. We insert explicit Cast(BF16→FP16) before Conv
      inputs and Cast(FP16→BF16) after Conv outputs. Conv weight/bias
      initializers are also kept in FP16.
    """
    fp32_path = output_dir / "vision_encoder_fp32.onnx"
    output_path = output_dir / "vision_encoder.onnx"

    # Download FP32 from ningpp/GLM-OCR
    if not fp32_path.exists() and not output_path.exists():
        print("Downloading vision_encoder.onnx from ningpp/GLM-OCR ...")
        from huggingface_hub import hf_hub_download
        hf_hub_download("ningpp/GLM-OCR", "vision_encoder.onnx",
                        local_dir=str(output_dir))
        # Rename to _fp32 so we can save the converted one as the main file
        (output_dir / "vision_encoder.onnx").rename(fp32_path)
        print(f"  Downloaded: {fp32_path.stat().st_size / 1024 / 1024:.0f} MB")
    elif not fp32_path.exists() and output_path.exists():
        print(f"vision_encoder.onnx already present ({output_path.stat().st_size / 1024 / 1024:.0f} MB)")
        return output_path

    if precision == "fp32":
        fp32_path.rename(output_path)
        _print_size(output_path)
        return output_path

    # Convert FP32 → FP16 (intermediate step for both fp16 and bf16)
    print("Converting vision_encoder to FP16 ...")
    model = onnx.load(str(fp32_path))

    # Block all Cast-to-FLOAT nodes to preserve FP32 upcast paths.
    # These nodes exist because certain ops (Conv, Range, LayerNorm) require
    # FP32 inputs. If we let onnxconverter_common convert their target dtype
    # to FP16, the Cast becomes a no-op and the downstream op gets FP16
    # input when it needs FP32, producing silent numerical errors.
    block_list = []
    for n in model.graph.node:
        if n.op_type == "Cast":
            for attr in n.attribute:
                if attr.name == "to" and attr.i == 1:  # TensorProto.FLOAT
                    block_list.append(n.name)
    print(f"  Blocking {len(block_list)} Cast-to-FLOAT nodes")

    with warnings.catch_warnings():
        warnings.simplefilter("ignore")
        model_fp16 = float16.convert_float_to_float16(
            model,
            keep_io_types=False,
            node_block_list=block_list,
        )

    if precision == "bf16":
        print("  Converting FP16 → BF16 (Conv ops stay FP16) ...")
        model_out = _convert_vision_fp16_to_bf16(model_fp16)
    else:
        model_out = model_fp16

    onnx.save(
        model_out, str(output_path),
        save_as_external_data=True,
        all_tensors_to_one_file=True,
        location="vision_encoder.onnx.data",
    )
    _print_size(output_path)

    # Clean up FP32 file
    fp32_path.unlink()
    return output_path


def _convert_vision_fp16_to_bf16(model):
    """Post-process FP16 vision encoder to BF16, keeping Conv nodes in FP16.

    ORT has no BF16 Conv kernel on any execution provider (CUDA, DirectML,
    CPU). Conv nodes must stay in FP16. This function:

    1. Converts ALL FP16 initializers, type annotations, and Cast nodes → BF16
    2. Reverts Conv weight/bias initializers back to FP16
    3. Inserts Cast(BF16→FP16) before each Conv input (for non-initializer inputs)
    4. Inserts Cast(FP16→BF16) after each Conv output

    The result is a BF16 graph with FP16 "islands" around each Conv op,
    connected by explicit Cast nodes.
    """
    from ml_dtypes import bfloat16 as ml_bf16

    FLOAT16 = onnx.TensorProto.FLOAT16    # 10
    BFLOAT16 = onnx.TensorProto.BFLOAT16  # 16

    # 1. Catalog Conv nodes and their I/O tensor names
    conv_input_names = set()
    conv_output_names = set()
    for node in model.graph.node:
        if node.op_type == "Conv":
            for name in node.input:
                if name:
                    conv_input_names.add(name)
            for name in node.output:
                conv_output_names.add(name)
    print(f"    Found {len(conv_output_names)} Conv node(s) to wrap")

    # 2. Convert ALL FP16 initializers to BF16
    # (We'll revert Conv-specific ones in step 5a)
    init_map = {}
    n_init = 0
    for init in model.graph.initializer:
        init_map[init.name] = init
        if init.data_type == FLOAT16:
            raw = np.frombuffer(init.raw_data, dtype=np.float16)
            init.raw_data = raw.astype(np.float32).astype(ml_bf16).tobytes()
            init.data_type = BFLOAT16
            n_init += 1

    # 3. Update ALL FP16 type annotations to BF16
    vi_map = {}
    n_vi = 0
    for vi in list(model.graph.value_info) + list(model.graph.input) + list(model.graph.output):
        vi_map[vi.name] = vi
        if vi.type.tensor_type.elem_type == FLOAT16:
            vi.type.tensor_type.elem_type = BFLOAT16
            n_vi += 1

    # 4. Update ALL Cast(to=FP16) to Cast(to=BF16)
    n_cast = 0
    for node in model.graph.node:
        if node.op_type == "Cast":
            for attr in node.attribute:
                if attr.name == "to" and attr.i == FLOAT16:
                    attr.i = BFLOAT16
                    n_cast += 1
    print(f"    Converted {n_init} initializers, {n_vi} type annotations, {n_cast} Cast nodes")

    # 5. Fix Conv nodes: keep them in FP16 with Cast wrappers
    # 5a. Convert Conv weight/bias initializers back to FP16
    for name in conv_input_names:
        if name in init_map and init_map[name].data_type == BFLOAT16:
            init = init_map[name]
            raw = np.frombuffer(init.raw_data, dtype=ml_bf16)
            init.raw_data = raw.astype(np.float32).astype(np.float16).tobytes()
            init.data_type = FLOAT16
            if name in vi_map:
                vi_map[name].type.tensor_type.elem_type = FLOAT16

    # 5b/5c. Insert Cast nodes around Conv ops
    # Rebuild node list to handle insertions cleanly
    original_nodes = list(model.graph.node)
    new_nodes = []

    for node in original_nodes:
        if node.op_type != "Conv":
            new_nodes.append(node)
            continue

        # Insert Cast(BF16→FP16) for each non-initializer BF16 input
        # (initializer inputs were already reverted to FP16 in step 5a)
        for i, inp_name in enumerate(node.input):
            if not inp_name or inp_name in init_map:
                continue
            if inp_name in vi_map and vi_map[inp_name].type.tensor_type.elem_type == BFLOAT16:
                cast_out = f"{inp_name}__to_fp16"
                cast_node = onnx.helper.make_node(
                    "Cast", inputs=[inp_name], outputs=[cast_out],
                    to=FLOAT16, name=f"Cast_bf16_to_fp16_{inp_name}",
                )
                new_vi = model.graph.value_info.add()
                new_vi.name = cast_out
                new_vi.type.tensor_type.elem_type = FLOAT16
                if vi_map[inp_name].type.tensor_type.HasField('shape'):
                    new_vi.type.tensor_type.shape.CopyFrom(
                        vi_map[inp_name].type.tensor_type.shape)
                node.input[i] = cast_out
                new_nodes.append(cast_node)

        # The Conv node itself (in FP16)
        new_nodes.append(node)

        # Insert Cast(FP16→BF16) for each output that downstream nodes expect as BF16
        for i, out_name in enumerate(node.output):
            if out_name in vi_map and vi_map[out_name].type.tensor_type.elem_type == BFLOAT16:
                fp16_name = f"{out_name}__conv_fp16"
                new_vi = model.graph.value_info.add()
                new_vi.name = fp16_name
                new_vi.type.tensor_type.elem_type = FLOAT16
                if vi_map[out_name].type.tensor_type.HasField('shape'):
                    new_vi.type.tensor_type.shape.CopyFrom(
                        vi_map[out_name].type.tensor_type.shape)
                cast_node = onnx.helper.make_node(
                    "Cast", inputs=[fp16_name], outputs=[out_name],
                    to=BFLOAT16, name=f"Cast_fp16_to_bf16_{out_name}",
                )
                node.output[i] = fp16_name
                new_nodes.append(cast_node)

    # Replace node list
    del model.graph.node[:]
    model.graph.node.extend(new_nodes)

    return model


def export_embedding(model, output_dir: Path):
    """Export the token embedding table as a standalone ONNX model.

    Input:  input_ids    [batch, seq_len] int64
    Output: inputs_embeds [batch, seq_len, 1536] float16/bf16

    Both batch and seq_len are dynamic axes, so this works for both
    prefill (seq_len = prompt length) and single-token decode steps.
    """
    print("Exporting embedding.onnx ...")
    wrapper = EmbedTokensWrapper(model).eval()

    input_ids = torch.randint(0, VOCAB_SIZE, (1, 32))

    output_path = output_dir / "embedding.onnx"
    torch.onnx.export(
        wrapper,
        (input_ids,),
        str(output_path),
        opset_version=17,
        input_names=["input_ids"],
        output_names=["inputs_embeds"],
        dynamic_axes={
            "input_ids": {0: "batch", 1: "seq_len"},
            "inputs_embeds": {0: "batch", 1: "seq_len"},
        },
    )
    _print_size(output_path)
    return output_path


def export_llm(model, output_dir: Path, dtype):
    """Export the LLM decoder with dynamic KV cache to ONNX.

    This model is used for the **prefill pass** — a single forward pass
    with the full prompt (text tokens + vision embeddings merged in).

    The KV cache uses dynamic past_seq_len (starts at 0 for prefill,
    grows by 1 each decode step). This makes it incompatible with CUDA
    graphs, which require fixed shapes. See export_decoder() and
    DecoderStepWrapper for the CUDA-graph-compatible alternative.

    Input tensors:
      - inputs_embeds   [batch, seq_len, 1536]  — merged text+vision embeddings
      - attention_mask   [batch, total_seq_len]  — 1s for all attended positions
      - position_ids     [3, batch, seq_len]     — M-RoPE 3D positions (t, h, w)
      - past_key_{0..15}   [batch, 8, past_seq_len, 128]  — GQA key cache
      - past_value_{0..15} [batch, 8, past_seq_len, 128]  — GQA value cache

    Output tensors:
      - logits             [batch, seq_len, 59392]   — vocabulary logits
      - present_key_{0..15}   [batch, 8, total_seq_len, 128]  — updated keys
      - present_value_{0..15} [batch, 8, total_seq_len, 128]  — updated values

    Note: position_ids has 3 dimensions for M-RoPE (temporal, height, width).
    For text tokens all 3 values are identical. For vision tokens they encode
    the 2D spatial position within the image grid.
    """
    print("Exporting llm.onnx ...")
    wrapper = LLMWrapper(model).eval().to(dtype)

    batch = 1
    seq_len = 8
    past_len = 4

    inputs_embeds = torch.randn(batch, seq_len, HIDDEN_SIZE, dtype=dtype)
    attention_mask = torch.ones(batch, past_len + seq_len, dtype=torch.long)
    # M-RoPE: 3D position IDs [3, batch, seq_len] — temporal, height, width
    position_ids = (torch.arange(past_len, past_len + seq_len)
                    .view(1, 1, -1).expand(3, batch, -1).contiguous().long())

    past_kv = []
    for _ in range(NUM_LAYERS):
        past_kv.append(torch.randn(batch, NUM_KV_HEADS, past_len, HEAD_DIM, dtype=dtype))
        past_kv.append(torch.randn(batch, NUM_KV_HEADS, past_len, HEAD_DIM, dtype=dtype))

    # Build names and dynamic axes
    input_names = ["inputs_embeds", "attention_mask", "position_ids"]
    output_names = ["logits"]
    dynamic_axes = {
        "inputs_embeds": {0: "batch", 1: "seq_len"},
        "attention_mask": {0: "batch", 1: "total_seq_len"},
        "position_ids": {1: "batch", 2: "seq_len"},
        "logits": {0: "batch", 1: "seq_len"},
    }

    for i in range(NUM_LAYERS):
        input_names.extend([f"past_key_{i}", f"past_value_{i}"])
        output_names.extend([f"present_key_{i}", f"present_value_{i}"])
        dynamic_axes[f"past_key_{i}"] = {0: "batch", 2: "past_seq_len"}
        dynamic_axes[f"past_value_{i}"] = {0: "batch", 2: "past_seq_len"}
        dynamic_axes[f"present_key_{i}"] = {0: "batch", 2: "total_seq_len"}
        dynamic_axes[f"present_value_{i}"] = {0: "batch", 2: "total_seq_len"}

    output_path = output_dir / "llm.onnx"
    all_inputs = (inputs_embeds, attention_mask, position_ids) + tuple(past_kv)

    torch.onnx.export(
        wrapper,
        all_inputs,
        str(output_path),
        input_names=input_names,
        output_names=output_names,
        dynamic_axes=dynamic_axes,
        opset_version=17,
        do_constant_folding=True,
    )
    _print_size(output_path)
    return output_path


# ── Helpers ───────────────────────────────────────────────────────────────


def _print_size(onnx_path: Path):
    onnx_size = onnx_path.stat().st_size / 1024 / 1024
    data_path = Path(str(onnx_path) + ".data")
    data_size = data_path.stat().st_size / 1024 / 1024 if data_path.exists() else 0
    total = onnx_size + data_size
    if data_size > 0:
        print(f"  {onnx_path.name}: {onnx_size:.1f} MB graph + {data_size:.1f} MB data = {total:.1f} MB")
    else:
        print(f"  {onnx_path.name}: {total:.1f} MB")


def _topological_sort(graph):
    """Fix ONNX node ordering after float16 conversion inserts Cast nodes.

    onnxconverter_common's float16 converter inserts new Cast nodes but
    doesn't maintain topological order. ORT requires nodes to be ordered
    such that all inputs are defined before they're consumed. This function
    performs a DFS-based topological sort to restore a valid ordering.
    """
    producers = {}
    for node in graph.node:
        for out in node.output:
            producers[out] = node

    input_names = set()
    for inp in graph.input:
        input_names.add(inp.name)
    for init in graph.initializer:
        input_names.add(init.name)

    visited = set()
    ordered = []

    def visit(node):
        node_id = id(node)
        if node_id in visited:
            return
        visited.add(node_id)
        for inp_name in node.input:
            if inp_name in producers and id(producers[inp_name]) not in visited:
                visit(producers[inp_name])
        ordered.append(node)

    for node in graph.node:
        visit(node)

    del graph.node[:]
    graph.node.extend(ordered)


def export_decoder(model, output_dir: Path, max_seq: int):
    """Export the CUDA-graph-friendly decode-step LLM.

    Produces llm_decoder.onnx with all-static shapes:
      - KV cache: [1, 8, MAX_SEQ, 128] (fixed, not growing)
      - Input: input_ids [1, 1] + step [1] + prefill_len [1]
      - Output: next_token [1] (ArgMax built into graph)

    No dynamic_axes — every tensor shape is fixed at export time. This is
    required for CUDA graph capture, which records a sequence of GPU kernel
    launches and replays them. Any shape change would invalidate the capture.

    The model weights are already FP16 (from main()), so no post-export
    FP16 conversion is needed. The export produces FP16 weights directly.
    """
    print(f"\nExporting llm_decoder.onnx (MAX_SEQ={max_seq}) ...")

    wrapper = DecoderStepWrapper(model, max_seq).eval().half()

    batch = 1
    seq = 1

    input_ids = torch.randint(0, VOCAB_SIZE, (batch, seq), dtype=torch.long)
    step = torch.tensor([0], dtype=torch.long)
    prefill_len = torch.tensor([10], dtype=torch.long)

    past_kv = []
    for _ in range(NUM_LAYERS):
        past_kv.append(torch.randn(batch, NUM_KV_HEADS, max_seq, HEAD_DIM, dtype=torch.float16))
        past_kv.append(torch.randn(batch, NUM_KV_HEADS, max_seq, HEAD_DIM, dtype=torch.float16))

    input_names = ["input_ids", "step", "prefill_len"]
    output_names = ["next_token"]

    for i in range(NUM_LAYERS):
        input_names.extend([f"past_key_{i}", f"past_value_{i}"])
        output_names.extend([f"present_key_{i}", f"present_value_{i}"])

    output_path = output_dir / "llm_decoder.onnx"
    all_inputs = (input_ids, step, prefill_len) + tuple(past_kv)

    torch.onnx.export(
        wrapper,
        all_inputs,
        str(output_path),
        input_names=input_names,
        output_names=output_names,
        opset_version=17,
        do_constant_folding=True,
    )

    _print_size(output_path)
    return output_path


def validate_decoder(output_dir: Path, max_seq: int):
    """Validate the exported decoder ONNX loads and produces correct shapes."""
    print("\nValidating llm_decoder.onnx ...")
    decoder_path = output_dir / "llm_decoder.onnx"

    opts = ort.SessionOptions()
    opts.log_severity_level = 3
    session = ort.InferenceSession(str(decoder_path), opts, providers=["CPUExecutionProvider"])

    print("  Session inputs:")
    for inp in session.get_inputs():
        print(f"    {inp.name}: {inp.type} {inp.shape}")
    print("  Session outputs:")
    for out in session.get_outputs():
        print(f"    {out.name}: {out.type} {out.shape}")

    # Test inference
    feed = {
        "input_ids": np.array([[100]], dtype=np.int64),
        "step": np.array([0], dtype=np.int64),
        "prefill_len": np.array([10], dtype=np.int64),
    }
    for i in range(NUM_LAYERS):
        feed[f"past_key_{i}"] = np.zeros(
            (1, NUM_KV_HEADS, max_seq, HEAD_DIM), dtype=np.float16
        )
        feed[f"past_value_{i}"] = np.zeros(
            (1, NUM_KV_HEADS, max_seq, HEAD_DIM), dtype=np.float16
        )

    outputs = session.run(None, feed)
    next_token = outputs[0]
    print(f"  next_token: {next_token} (shape={next_token.shape}, dtype={next_token.dtype})")

    for i in range(NUM_LAYERS):
        k = outputs[1 + i * 2]
        v = outputs[2 + i * 2]
        assert k.shape == (1, NUM_KV_HEADS, max_seq, HEAD_DIM), \
            f"present_key_{i} shape mismatch: {k.shape}"
        assert v.shape == (1, NUM_KV_HEADS, max_seq, HEAD_DIM), \
            f"present_value_{i} shape mismatch: {v.shape}"

    print("  PASS: all shapes correct")


def validate_llm(wrapper, output_dir: Path, dtype):
    """Validate LLM ONNX output matches PyTorch within precision tolerance.

    Runs both models on the same random input and checks that the logits
    max absolute difference is within tolerance:
      - FP16: < 1.0 (FP16 has limited precision, large logit magnitudes)
      - FP32: < 0.01
    """
    print("\nValidating LLM ...")
    output_path = output_dir / "llm.onnx"
    opts = ort.SessionOptions()
    opts.log_severity_level = 3
    session = ort.InferenceSession(str(output_path), opts, providers=["CPUExecutionProvider"])

    print(f"  Prefill test (dtype={dtype}) ...", flush=True)
    embeds = torch.randn(1, 16, HIDDEN_SIZE, dtype=dtype)
    mask = torch.ones(1, 16, dtype=torch.long)
    pos = torch.arange(16).view(1, 1, -1).expand(3, 1, -1).contiguous().long()
    empty_kv = [torch.zeros(1, NUM_KV_HEADS, 0, HEAD_DIM, dtype=dtype)
                for _ in range(NUM_LAYERS * 2)]

    wrapper = wrapper.to(dtype)
    with torch.no_grad():
        pt_out = wrapper(embeds, mask, pos, *empty_kv)

    onnx_feed = {
        "inputs_embeds": embeds.numpy(),
        "attention_mask": mask.numpy(),
        "position_ids": pos.numpy(),
    }
    for i in range(NUM_LAYERS):
        onnx_feed[f"past_key_{i}"] = empty_kv[i * 2].numpy()
        onnx_feed[f"past_value_{i}"] = empty_kv[i * 2 + 1].numpy()

    onnx_out = session.run(None, onnx_feed)
    diff = np.abs(pt_out[0].float().numpy() - onnx_out[0].astype(np.float32)).max()
    threshold = 1.0 if dtype == torch.float16 else 0.01
    print(f"    logits max diff: {diff:.2e} {'PASS' if diff < threshold else 'FAIL'}")


# ── Main ──────────────────────────────────────────────────────────────────


def main():
    parser = argparse.ArgumentParser(description="Export GLM-OCR to ONNX")
    parser.add_argument("--output-dir", type=str, default=DEFAULT_OUTPUT_DIR)
    dtype_group = parser.add_mutually_exclusive_group()
    dtype_group.add_argument("--fp32", action="store_true",
                             help="Export in FP32 (debug)")
    dtype_group.add_argument("--bf16", action="store_true",
                             help="Export all models in BF16 (requires CUDA)")
    parser.add_argument("--skip-vision", action="store_true",
                        help="Skip vision encoder export (use existing file)")
    parser.add_argument("--skip-decoder", action="store_true",
                        help="Skip CUDA-graph decoder export")
    parser.add_argument("--max-seq", type=int, default=DEFAULT_MAX_SEQ,
                        help=f"Max sequence length for decoder KV cache (default: {DEFAULT_MAX_SEQ})")
    args = parser.parse_args()

    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    # Determine export dtype for embedding + LLM
    if args.fp32:
        export_dtype = torch.float32
    elif args.bf16:
        export_dtype = torch.bfloat16
    else:
        export_dtype = torch.float16

    print(f"Export dtype: {export_dtype}")
    print(f"Loading {MODEL_ID} (native BF16) ...")
    t0 = time.perf_counter()
    model = AutoModelForImageTextToText.from_pretrained(MODEL_ID)  # loads as BF16
    model.eval()
    if export_dtype != torch.bfloat16:
        model.to(export_dtype)  # BF16 -> FP16 or FP32
    print(f"Loaded in {time.perf_counter() - t0:.1f}s\n")

    # Save processor/tokenizer/config for inference.
    # These files are needed by both Python (AutoProcessor.from_pretrained)
    # and Rust (reads tokenizer.json directly) inference code. They are
    # downloaded from HuggingFace and saved locally so inference doesn't
    # need network access.
    processor = AutoProcessor.from_pretrained(MODEL_ID)
    processor.save_pretrained(str(output_dir))
    config = AutoConfig.from_pretrained(MODEL_ID)
    config.save_pretrained(str(output_dir))
    print(f"Saved processor and config to {output_dir}\n")

    with torch.no_grad():
        # ── Shared models (used by both prefill and non-CUDA-graph decode) ──
        export_embedding(model, output_dir)

        # ── Prefill LLM (dynamic KV cache, runs once per image) ──
        export_llm(model, output_dir, export_dtype)

        # ── Vision encoder (downloaded + converted, not re-exported) ──
        if args.skip_vision:
            print("Skipping vision encoder (--skip-vision)")
        else:
            if args.fp32:
                vision_precision = "fp32"
            elif args.bf16:
                vision_precision = "bf16"
            else:
                vision_precision = "fp16"
            export_vision_encoder(output_dir, precision=vision_precision)

        # ── Decode-step LLM (fixed KV cache, CUDA graph compatible) ──
        if args.skip_decoder:
            print("\nSkipping decoder (--skip-decoder)")
        else:
            export_decoder(model, output_dir, args.max_seq)

    # ── Validation ──
    if args.bf16:
        print("\nSkipping validation (BF16 requires CUDA)")
    else:
        llm_wrapper = LLMWrapper(model).eval()
        validate_llm(llm_wrapper, output_dir, export_dtype)
        if not args.skip_decoder:
            validate_decoder(output_dir, args.max_seq)

    # ── Summary ──
    print(f"\n{'='*50}")
    print("Export complete. Files:")
    total = 0
    for f in sorted(output_dir.glob("*.onnx*")):
        if f.suffix in ('.onnx', '.data'):
            sz = f.stat().st_size / 1024 / 1024
            total += sz
            print(f"  {f.name:40s} {sz:>8.1f} MB")
    print(f"  {'TOTAL':40s} {total:>8.1f} MB")
    if args.bf16:
        print("\nNote: BF16 models require CUDA. Conv ops in vision encoder stay FP16.")


if __name__ == "__main__":
    main()
